def ptrn_convert(cmap, width=6, height=16):
    m=[0]*16*128
    for ch, bmap in cmap.items():
        ls=bmap.splitlines()
        print(f'Char {ch} ({ord(ch):02X}), {len(ls)} lines ')
        for l_nr, l in enumerate(ls):
            pattern=0
            bval=1<<(width-1)
            for p in l:
                if p is not ' ': pattern+=bval
                bval>>=1
                if bval==0: break;
            m[ord(ch)*height+l_nr -  (0 if len(ls) <= 12  else 1) ]=pattern

    #print(m)

    with open("charset_code.c",'w') as wf:
        nl_count=0

        cs=''
        for val in m:
            if cs: cs+=','
            if nl_count>=32:
                nl_count=0
                cs+='\n'
            cs+="0x%02x"%val
            nl_count+=1


        #hx=f"\n\nconst uint8_t charset16x6[]="+"{"+",".join(["0x%02x"%v for v in m])+"};\n\n"
        hx=f"\n\nconst uint8_t charset16x6[]="+"{\n"+cs+"\n};\n\n"
        print( hx )
        wf.write(hx)


